package Latihan01.sesi1.bin;

public class CobaOperator03{
	
	public static void main(String[] satria){
		int c=3,d=5,e=4,f=6,g=12;
		System.out.println("Nilai awal c adalah " + c);
		System.out.println("Nilai c+=7   adalah " + (c+=7));
		System.out.println("Nilai c      adalah " + c);
		System.out.println();
		
		System.out.println("Nilai awal d adalah " + d);
		System.out.println("Nilai d-=4   adalah " + (d-=4));
		System.out.println("Nilai d      adalah " + d);
		System.out.println();
		
		System.out.println("Nilai awal e adalah " + e);
		System.out.println("Nilai e*=5   adalah " + (e*=5));
		System.out.println("Nilai e      adalah " + e);
		System.out.println();
		
		System.out.println("Nilai awal f adalah " + f);
		System.out.println("Nilai f/=5   adalah " + (f/=5));
		System.out.println("Nilai f      adalah " + f);
		System.out.println();
		
		System.out.println("Nilai awal g adalah " + g);
		System.out.println("Nilai g%=7   adalah " + (g%=7));
		System.out.println("Nilai g      adalah " + g);		
	}	
}